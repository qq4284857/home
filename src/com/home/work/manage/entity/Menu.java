package com.home.work.manage.entity;

public class Menu {
	private String id;
	
	private String name;
	
	private String url;
	
	private String userid;
	
	private int mark;
	
	private String time;
	
	private String menu;
	
	private String er_menu;
	
	

	public String getMenu() {
		return menu;
	}

	public void setMenu(String menu) {
		this.menu = menu;
	}

	public String getEr_menu() {
		return er_menu;
	}

	public void setEr_menu(String er_menu) {
		this.er_menu = er_menu;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}



	public int getMark() {
		return mark;
	}

	public void setMark(int mark) {
		this.mark = mark;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	

}
