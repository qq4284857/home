package com.home.work.menu.entity;

public class Menus {
	
	private String id;
	
	private int uid;
	
	private String username;
	
	private String password;
	
	private String mid;
	
	private String name;
	
	private String url;
	
	private int sturts;
	
	private int flag;
	
	private String er_menu;
	
	

	
	public String getEr_menu() {
		return er_menu;
	}

	public void setEr_menu(String er_menu) {
		this.er_menu = er_menu;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getMid() {
		return mid;
	}

	public void setMid(String mid) {
		this.mid = mid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getSturts() {
		return sturts;
	}

	public void setSturts(int sturts) {
		this.sturts = sturts;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}
	
	
}
